using System.Collections.Generic;
using UnityEngine;

public class Clima : MonoBehaviour
{
    public static Clima Instance;

    private void Awake()
    {
        if (Instance == null)
        {
            Instance = this;
        }
        else
        {
            if (Instance != this)
            {
                Destroy(this.gameObject);
            }
        }
    }
